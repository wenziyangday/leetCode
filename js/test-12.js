function findNameByKey(treeData, targetKey) {
	for (const node of treeData) {
		if (node.key === targetKey) return node.appId; // 当前节点匹配直接返回
		if (node.children) {
			const res = findNameByKey(node.children, targetKey);
			if (res) return res; // 子节点中找到后立即终止递归
		}
	}
	return undefined; // 未找到
}

